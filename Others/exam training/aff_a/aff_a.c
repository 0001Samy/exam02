#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(int ac, char **av)
{
	(void)ac;
	(void)av;

	ft_putchar('a');
	ft_putchar('\n');
	return (0);
}
